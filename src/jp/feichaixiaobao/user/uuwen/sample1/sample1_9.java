package jp.feichaixiaobao.user.uuwen;

/*
 練習問題 1 - 9
 int 型の変数 x に任意の数値を代入し、x を 2 倍、3 倍、4 倍した結果を表示するプログラムを作成しなさい。
 */

public class sample1_9 {

	public static void main(String[] args) {
		int x = 2;
		System.out.println("x を 2 倍, x = " + x * 2 );
		System.out.println("x を 3 倍, x = " + x * 3 );
		System.out.println("x を 4 倍, x = " + x * 4 );

	}

}
