package jp.feichaixiaobao.user.mori;

public class java1_9 {

	public static void main(String[] args) {
		/*
		 * int 型の変数 x に任意の数値を代入し、
		 * x を 2 倍、3 倍、4 倍した結果を表示するプログラムを作成しなさい。
		 */
		int x = 2;
		System.out.println("xを2倍した結果は" + x*2);
		System.out.println("xを3倍した結果は" + x*3);
		System.out.println("xを4倍した結果は" + x*4);

	}

}
