public class AutoConvert {
    public static void main(String[] args) {
        int num = 'a';
        double d1 = 80;
        System.out.println(num);
        System.out.println(d1);

        int n1 = 10;
        float d2 = n1 + 1.1F;
        byte b1 = 10;

        byte b2 = 1;
        byte b3 = 2;
        short s1 = 1;
        int s2 = b2 + s1;
    }
}
