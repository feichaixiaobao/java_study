public class AthimeticOperator {
    public static void main(String[] args) {
        System.out.println(10/4);
        double d = 10 / 4;
        System.out.println(d);
        System.out.println(10.0 / 4);

        System.out.println(10 % 3);
        System.out.println(-10 % 3);
        System.out.println(10 % -3);
        System.out.println(-10 % -3);
        // 在%的本质 看一个公式 a % b = a - a / b * b
    }
}
